/**
 * Look up a host adapter by id. Returns undefined for unknown hosts.
 *
 * @public
 */
export function getHostAdapter(id: string): HostAdapter | undefined {
  return HOST_ADAPTERS.find((h) => h.id === id);
}

/**
 * Valid --host CLI values (in addition to "all").
 *
 * @public
 */
export const SUPPORTED_HOST_IDS = HOST_ADAPTERS.map((h) => h.id);
