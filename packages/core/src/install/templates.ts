/**
 * Per-host adapter — knows the file path + format for the rule content.
 *
 * The Markdown hosts (CLAUDE.md / AGENTS.md / GEMINI.md / CONVENTIONS.md /
 * copilot-instructions.md) carry the canonical RULES_BLOCK_CONTENT
 * wrapped in HMAC markers. The native-format hosts (.cursorrules /
 * .windsurfrules use YAML) need a transcoded body but the same
 * underlying instructions.
 *
 * @public
 */
export interface HostAdapter {
  /** Slug used on the --host CLI flag. */
  id: string;
  /** Relative file path written under the project root. */
  path: string;
  /** Whether this host is included in the default install (no --host flag). */
  defaultEnabled: boolean;
  /**
   * Render the FULL file content for this host, given the canonical
   * rules text. Returns the bytes that will land on disk.
   */
  render(): string;
  /**
   * Verify on-disk content matches what `render()` would produce now.
   * Used for idempotency checks. Markdown adapters defer to HMAC
   * block extraction; YAML/JSON adapters compare verbatim.
   */
  isCanonical(currentContent: string): boolean;
}

/**
 * Strip Markdown formatting that's noise in a plain-text rules file
 * (CONVENTIONS.md works as Markdown, but Aider/Cursor users often
 * scan with plain `cat`; the rendered file should read cleanly without
 * a viewer).
 */
function plainBody(): string {
  return RULES_BLOCK_CONTENT
    .replace(/^##\s+/gm, '')           // strip h2 markers
    .replace(/^###\s+/gm, '')          // strip h3 markers
    .replace(/^####\s+/gm, '')         // strip h4 markers
    .replace(/\*\*(.*?)\*\*/g, '$1')   // strip bold
    .replace(/`([^`]+)`/g, '$1');      // strip code fences
}

